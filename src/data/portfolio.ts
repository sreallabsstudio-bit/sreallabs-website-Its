// Helper functions
export function getProjectById(id: string): PortfolioProject | undefined {
  return portfolio.find((p) => p.id === id);
}

export function getProjectBySlug(slug: string): PortfolioProject | undefined {
  return portfolio.find((p) => p.slug === slug);
}

export function getFeaturedProjects(): PortfolioProject[] {
  return portfolio.filter((p) => p.featured);
}

export function getProjectsByCategory(category: PortfolioCategory): PortfolioProject[] {
  if (category === "All") return portfolio;
  return portfolio.filter((p) => p.category === category);
}

export function getRelatedProjects(project: PortfolioProject): PortfolioProject[] {
  return project.relatedProjectIds
    .map((id) => getProjectById(id))
    .filter((p): p is PortfolioProject => p !== undefined);
}

export function getProjectsByService(service: string): PortfolioProject[] {
  return portfolio.filter((p) => p.service === service);
}